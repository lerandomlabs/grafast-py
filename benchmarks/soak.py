"""Full concurrent soak: 5000 ops at concurrency 32 over a mixed query workload.

Seeds ``grafast_demo`` once at N=200 authors (~5200 rows), then drives a validated
mix of nested + connection + hasOne + by-id GraphQL operations concurrently through
:class:`grafast_py.GrafastExecutionContext`, asserting under concurrency:

(a) ZERO errors / 100% correct results;
(b) NO connection-pool leak (checkouts return to baseline, never exceed pool max);
(c) NO memory leak (RSS drift after warmup < threshold);
(d) bounded latency (p50/p95/p99 reported).

Writes ``bench/soak_results.md`` and prints a summary. The whole run is one
``asyncio.run`` so a single shared engine/pool serves every op (the only correct,
leak-testable shape). Pool stays at the default 15-max — CONCURRENCY 32 intentionally
exceeds it so checkouts queue, exercising the leak check.

SAFETY: only ``grafast_py_test`` / schema ``grafast_demo`` is touched (the engine
hard-codes the URL; the seeder imports it and never constructs its own).

Run:  uv run python bench/soak.py
"""

import asyncio
import sys
from pathlib import Path

# run as a script: put the repo root on sys.path so soak_core can import the
# `examples` demo fixtures as `examples.*`.
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from grafast_py.pg.engine import dispose_engine
from soak_core import run_soak, scale_seed, SoakResult

CONCURRENCY = 32
TOTAL_OPS = 5000
WARMUP_OPS = 500
N_AUTHORS = 200
RSS_THRESHOLD_MB = 50.0

# soft latency bounds (reported + flagged, not crash gates — the hard gates are
# errors==0, no pool leak, bounded RSS; latency is contention-aware monitoring).
# 32 coroutines sharing a 15-conn pool serialise ~2x at the pool, and the nested
# shape materialises ~4k rows per op (single-op p50 ~50ms); measured soak p95 ~400ms
# / p99 ~450ms is genuine queuing, not per-row work (single-op SQL is O(depth):
# 1/3/2/2/2). Bounds set above the measured envelope so a real stall (seconds) still
# trips the flag.
P95_SOFT_MS = 600.0
P99_SOFT_MS = 800.0

RESULTS_MD = Path(__file__).parent / "soak_results.md"


def render_md(res: SoakResult) -> str:
    lines = ["# Concurrent soak results", ""]
    lines.append(
        "Generated by `bench/soak.py`. One event loop, one shared engine/pool"
        f" (default {res.pool_max}-max); CONCURRENCY={res.concurrency} intentionally"
        " exceeds the pool so checkouts queue. Dataset: N="
        f"{N_AUTHORS} authors (fixed fan-out 5 posts/author, 4 comments/post)."
        f" Warmup {WARMUP_OPS} ops discarded before measuring."
    )
    lines.append("")
    lines.append("## Workload + correctness")
    lines.append("")
    lines.append("| metric | value |")
    lines.append("|---|---|")
    lines.append(f"| total ops (post-warmup) | {res.total_ops} |")
    lines.append(f"| concurrency | {res.concurrency} |")
    lines.append("| query shapes | flat, nested, hasOne, connection, by-id |")
    lines.append(f"| errors | {res.errors} |")
    lines.append(
        f"| correct | {res.correct} / {res.total_ops}"
        f" ({100.0 * res.correct / res.total_ops:.1f}%) |"
    )
    lines.append("")
    lines.append("## Connection pool (no leak)")
    lines.append("")
    lines.append("| metric | value |")
    lines.append("|---|---|")
    lines.append(f"| pool max | {res.pool_max} |")
    lines.append(
        f"| baseline checkedout / checkedin / size |"
        f" {res.baseline_checkedout} / {res.baseline_checkedin} / {res.baseline_size} |"
    )
    lines.append(f"| max checkedout during soak | {res.max_checkedout} |")
    lines.append(f"| final checkedout | {res.final_checkedout} |")
    lines.append(f"| final size | {res.final_size} |")
    lines.append(f"| pool leak | {res.pool_leak} |")
    lines.append("")
    lines.append("## Memory (no leak)")
    lines.append("")
    lines.append("| metric | value (MB) |")
    lines.append("|---|---|")
    lines.append(f"| rss start | {res.rss_start:.1f} |")
    lines.append(f"| rss after warmup | {res.rss_warmup:.1f} |")
    lines.append(f"| rss final | {res.rss_final:.1f} |")
    lines.append(f"| rss peak | {res.rss_peak:.1f} |")
    lines.append(
        f"| rss drift (final - warmup) | {res.rss_drift:.1f}"
        f" (threshold {RSS_THRESHOLD_MB:.0f}) |"
    )
    lines.append("")
    lines.append("## Latency (post-warmup, ms)")
    lines.append("")
    lines.append("| p50 | p95 | p99 | max |")
    lines.append("|---|---|---|---|")
    lines.append(
        f"| {res.p50_ms:.2f} | {res.p95_ms:.2f} | {res.p99_ms:.2f} | {res.max_ms:.2f} |"
    )
    lines.append("")
    passed = res.passed(RSS_THRESHOLD_MB)
    lines.append(
        f"**Soak verdict:** {'PASS' if passed else 'FAIL'} — "
        f"errors={res.errors}, correct={res.correct}/{res.total_ops}, "
        f"pool_leak={res.pool_leak}, rss_drift={res.rss_drift:.1f}MB"
        f" (< {RSS_THRESHOLD_MB:.0f}). Latency p95={res.p95_ms:.1f}ms"
        f" (soft {P95_SOFT_MS:.0f}), p99={res.p99_ms:.1f}ms (soft {P99_SOFT_MS:.0f})."
    )
    lines.append("")
    return "\n".join(lines)


async def main() -> None:
    print(f"concurrent soak — {TOTAL_OPS} ops @ concurrency {CONCURRENCY}, N={N_AUTHORS}")
    print("=" * 72)
    await dispose_engine()  # fresh engine bound to THIS loop
    await scale_seed(N_AUTHORS)
    try:
        res = await run_soak(
            total_ops=TOTAL_OPS,
            concurrency=CONCURRENCY,
            n_authors=N_AUTHORS,
            warmup_ops=WARMUP_OPS,
            rss_threshold_mb=RSS_THRESHOLD_MB,
        )
    finally:
        await dispose_engine()

    print(f"errors={res.errors}  correct={res.correct}/{res.total_ops}")
    print(
        f"pool: max_checkedout={res.max_checkedout} final_checkedout="
        f"{res.final_checkedout} (max {res.pool_max})  leak={res.pool_leak}"
    )
    print(
        f"rss: warmup={res.rss_warmup:.1f} final={res.rss_final:.1f} "
        f"drift={res.rss_drift:.1f}MB peak={res.rss_peak:.1f}"
    )
    print(
        f"latency ms: p50={res.p50_ms:.2f} p95={res.p95_ms:.2f} "
        f"p99={res.p99_ms:.2f} max={res.max_ms:.2f}"
    )
    print("=" * 72)

    RESULTS_MD.write_text(render_md(res))
    print(f"wrote {RESULTS_MD}")

    if res.p95_ms >= P95_SOFT_MS or res.p99_ms >= P99_SOFT_MS:
        print(
            f"NOTE: latency soft bound exceeded (p95={res.p95_ms:.1f},"
            f" p99={res.p99_ms:.1f}) — investigate contention."
        )

    assert res.errors == 0, f"soak had {res.errors} errors: {res.error_messages[:5]}"
    assert not res.pool_leak, (
        f"pool leak: max_checkedout={res.max_checkedout} "
        f"final={res.final_checkedout} size {res.final_size} vs base {res.baseline_size}"
    )
    assert res.rss_drift < RSS_THRESHOLD_MB, (
        f"rss drift {res.rss_drift:.1f}MB >= {RSS_THRESHOLD_MB}MB"
    )
    print("CHECKPOINT OK: errors==0, no pool leak, rss bounded.")


if __name__ == "__main__":
    asyncio.run(main())
